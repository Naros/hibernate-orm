package org.hibernate.orm.test.tool.schemaupdate.manytomany;

import java.util.EnumSet;

import org.hibernate.orm.test.tool.BaseSchemaUnitTestCase;
import org.hibernate.tool.schema.TargetType;

import org.hibernate.testing.TestForIssue;
import org.hibernate.testing.junit5.schema.SchemaScope;
import org.hibernate.testing.junit5.schema.SchemaTest;

import static org.hamcrest.MatcherAssert.assertThat;
import static org.hamcrest.core.Is.is;

/**
 * @author Andrea Boriero
 */
public class ForeignKeyNameTest extends BaseSchemaUnitTestCase {

	@Override
	protected boolean createSqlScriptTempOutputFile() {
		return true;
	}

	@Override
	protected boolean dropSchemaAfterTest() {
		return false;
	}

	@Override
	protected String[] getHmbMappingFiles() {
		return new String[] { "tool/schemaupdate/manytomany/UserGroup.hbm.xml" };
	}

	@SchemaTest
	@TestForIssue(jiraKey = "HHH-10247")
	public void testJoinedSubclassForeignKeyNameIsNotAutoGeneratedWhenProvided(SchemaScope schemaScope) throws Exception {
		schemaScope.withSchemaExport( schemaExport ->
								  schemaExport
										  .setDelimiter( ";" )
										  .setFormat( true )
										  .create( EnumSet.of( TargetType.SCRIPT ) ) );

		final String fileContent = getSqlScriptOutputFileContent();
		assertThat( fileContent.toLowerCase().contains( "fk_user_group" ), is( true ) );
		assertThat( fileContent.toLowerCase().contains( "fk_group_user" ), is( true ) );
	}
}
